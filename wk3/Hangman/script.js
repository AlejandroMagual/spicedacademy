// Function "generateRandomIndex" randomly generates array-index to select word to be guessed
// from "wordArray". The function is called when "START" button is clicked on (see function "START").
function generateRandomIndex() {
    // returns random integer value from 0 to (wordArray.length - 1)
    return Math.round((Math.random())*(wordArray.length)) - 1;
}
